// Write a program that reads an integer, a floating point, a double, a character, a long number.

#include <iostream>

using namespace std;

int main()
{
    int a;

    float b;

    double c;

    long long d;

    char ch;

    cin >> a >> b >> c >> d >> ch;

    cout << "Interger value: " << a << endl;

    cout << "Floating Point value: " << b << endl;

    cout << "Double value: " << c << endl;

    cout << "Long value: " << d << endl;

    cout << "Character: " << ch << endl;

    return 0;
}